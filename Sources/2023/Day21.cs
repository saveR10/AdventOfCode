        public void Exploring()
        {
            for (int r = 0; r < n; r++)
            {
                for (int c = 0; c < n; c++)
                {
                    if (YouAre[r, c])
                    {
                        if (r > 0 && Garden[r - 1, c] != '#') NextYou[r - 1, c] = true;
                        if (c < n - 1 && Garden[r, c + 1] != '#') NextYou[r, c + 1] = true;
                        if (r < n - 1 && Garden[r + 1, c] != '#') NextYou[r + 1, c] = true;
                        if (c > 0 && Garden[r, c - 1] != '#') NextYou[r, c - 1] = true;
                    }
                }
            }

            YouAre = NextYou;
            NextYou = new bool[n, n];
        }
