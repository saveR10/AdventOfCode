        public int OtherHand(int index)
        {
            if (index < 0)
                return n - 1;
            if (index == n)
                return 0;
            else return index;
        }
        public int[] ChangeMap(int r, int c, int rm, int cm)
        {
            int[] ret = new int[4];
            ret[0] = r;
            ret[1] = c;
            ret[2] = rm;
            ret[3] = cm;

            if (r < 0) { ret[2]--; ret[0] = n - 1; }
            if (c < 0) { ret[3]--; ret[1] = n - 1; }
            if (r == n) { ret[2]++; ret[0] = 0; }
            if (c == n) { ret[3]++; ret[1] = 0; }
            return ret;
        }


